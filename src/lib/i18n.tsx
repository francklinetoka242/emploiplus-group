const DICTS: Record<Locale, Dict> = { fr: FR, en: EN, ln: LN, es: ES, sw: SW, pt: PT, zh: ZH };

type Ctx = { locale: Locale; setLocale: (l: Locale) => void; t: (k: string) => string };
const I18nContext = createContext<Ctx>({ locale: "fr", setLocale: () => {}, t: (k) => k });

function detectInitial(): Locale {
  if (typeof window === "undefined") return "fr";
  try {
    const saved = window.localStorage.getItem("epg.locale") as Locale | null;
    if (saved && Object.keys(DICTS).includes(saved)) return saved;
    const nav = (navigator.language || "fr").toLowerCase();
    if (nav.startsWith("en")) return "en";
    if (nav.startsWith("ln")) return "ln";
    if (nav.startsWith("es")) return "es";
    if (nav.startsWith("sw")) return "sw";
    if (nav.startsWith("pt")) return "pt";
    if (nav.startsWith("zh")) return "zh";
    return "fr";
  } catch {
    return "fr";
  }
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("fr");
  useEffect(() => {
    setLocaleState(detectInitial());
  }, []);
  const setLocale = (l: Locale) => {
    setLocaleState(l);
    if (typeof window !== "undefined") window.localStorage.setItem("epg.locale", l);
  };
  const t = (k: string) => DICTS[locale][k] ?? DICTS["en"][k] ?? FR[k] ?? k;
  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>;
}

export const useI18n = () => useContext(I18nContext);
